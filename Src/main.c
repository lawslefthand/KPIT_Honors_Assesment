void led_dimmer_test(void) {

	int i = 0;

	while (i <= 41900) {
		TIM1->CCR1 = i;
		i += 100;
		HAL_Delay(10);
	}
	while (i >= 0) {
		TIM1->CCR1 = i;
		i -= 100;
		HAL_Delay(10);
	}
}

void map_duty_to_ldr(float ldr_value, int offset) {

	if (ldr_value > 4095) {
		ldr_value = 4095;
	}

	float mapped_value = ldr_value * 10.25;
	printf("LDR Sensor value is %f\r\n Mapped PWM ARR value is %f\r\n",
			ldr_value, mapped_value);
	TIM1->CCR1 = mapped_value - offset;

}

